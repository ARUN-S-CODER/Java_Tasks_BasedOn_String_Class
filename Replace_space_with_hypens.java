import java.util.*;

/*
        Write a program that replaces all spaces in a string with hypens.
        Input:  "Hello World Welcome you"
        Output: "Hello-World-Welcome-you"
*/

public class Replace_space_with_hypens {
    public static void main(String[] args) {
        System.out.print((new Scanner(System.in)).nextLine().replace(" ", "-")); // One line code
    }

}
